b=7
for i in range(6):
    b=b+i
    print("HI")
print(b)
# This code initializes a variable `b` to 7, then iterates 6 times, adding the current index `i` to `b` in each iteration.
# The loop runs from 0 to 5, so `b` will be incremented by the sum of these indices (0+1+2+3+4+5 = 15).
# After the loop, `b` will be 22 (7 + 15)